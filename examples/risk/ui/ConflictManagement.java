    private void conflictTableMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_conflictTableMouseClicked
        // TODO add your handling code here:

        int row = conflictTable.rowAtPoint(evt.getPoint());
        DefaultTableModel model = (DefaultTableModel) conflictTable.getModel();
        int id = (int) model.getValueAt(row, 0);
        Conflicts conflict = (Conflicts) ConflictController.show(id);
        setConflictOnTable(conflict);
        editStatus();
    }//GEN-LAST:event_conflictTableMouseClicked

    private void updateBtnMouseReleased(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_updateBtnMouseReleased
        // TODO add your handling code here:
        if(validateData()){
            ConflictController.update(getConflictFromTable());
            updateTable();
        }
    }//GEN-LAST:event_updateBtnMouseReleased

    private void newBtnMouseReleased(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_newBtnMouseReleased
        // TODO add your handling code here:
        if(validateData()){
            //need to fix project
            Projects tempProject = ProjectController.index().get(0);
            Conflicts project = new ConflictBuilder()
                    .addCode(codeField.getText())
                    .addMethod1(methods.get(method1Cb.getSelectedIndex()))
                    .addMethod2(methods.get(method2Cb.getSelectedIndex()))
                    .addProject(tempProject)
                    .build();
            ConflictController.create(project);
            updateTable();
        }
    }//GEN-LAST:event_newBtnMouseReleased

