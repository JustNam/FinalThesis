    public boolean validateData(){
        String error = new String();
        ArrayList<String> errorArr = new ArrayList<>();
        if (codeField.getText().equals("")){
            errorArr.add("code field");
        }
        
        if (errorArr.size() > 0){
            error = "Please fill the ";
            for (int i = 0; i < errorArr.size() - 1; i++) {
                error += errorArr.get(i) + ", ";
            }
            error += errorArr.get(errorArr.size()-1)+ "!";
            
            JOptionPane.showMessageDialog(new JFrame(),
            error,"Warning",
            JOptionPane.WARNING_MESSAGE);
            return false;
        } else {
            return true;
        }
    }
    public void setConflictOnTable(Conflicts conflict){
        idLabel.setText(Integer.toString(conflict.getId()));
        codeField.setText(conflict.getCode());
        method1Cb.setSelectedItem(conflict.getMethodsByMethod1Id().getCode());
        method2Cb.setSelectedItem(conflict.getMethodsByMethod2Id().getCode());
    }
    public Conflicts getConflictFromTable(){
        Conflicts conflict = new ConflictBuilder()
                .addId(Integer.valueOf(idLabel.getText()))
                .addCode(codeField.getText())
                .addMethod1(methods.get(method1Cb.getSelectedIndex()))
                .addMethod2(methods.get(method2Cb.getSelectedIndex()))
                .build();
        return conflict;
    }
    
    public void updateTable(){
        DefaultTableModel model = (DefaultTableModel) conflictTable.getModel();
        model.setRowCount(0);
        conflicts = ConflictController.index();
        methods = MethodController.index();
        for (int i = 0; i < conflicts.size(); i++) {
            Conflicts tempConflict = (Conflicts) conflicts.get(i);
            model.addRow(new Object[] {
                tempConflict.getId(),
                tempConflict.getCode(),
                tempConflict.getMethodsByMethod1Id().getCode(),
                tempConflict.getMethodsByMethod2Id().getCode(),
            });
        }
    }
